package Demoalljavascenario;

public class Demo07SwapString {

	public static void main(String[] args) {

		String a = "Hello";
		String b = "World";
		
		//Using Third datatype
		String c ="";
		c=a;
		a=b;
		b=c;
		System.out.println("Using Third datatype a: "+a);
		System.out.println("Using Third datatype b: "+b);
		
		
		
		//
		
		
		
	}

}
